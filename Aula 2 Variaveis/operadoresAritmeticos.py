numero = int(input("Digite um número: "))
numero2 = int(input("Digite um número: "))

#operador de adição: 
soma = numero + numero2
print("O resultado da soma é igual a " , soma)

#operador de subtração:
subtracao = numero - numero2
print("O resultado dessa subtração é: " , subtracao)

#operador de multiplicação:
multiplicacao = numero * numero2
print("O resultado dessa multiplicação é: " , multiplicacao)

#operador de potência:
potencia = numero ** numero2
print("O resultado da potência é igual a: " , potencia)

#operação de divisão por inteiro
divisaoInteira = numero // numero2
print("O resultado da divisão por inteiro é igual a: " , divisaoInteira)

#Operador de módulo:
modulo = numero % numero2
print("O resultado da operação módulo é igual a: " , modulo)